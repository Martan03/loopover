/// Gets cell with given number
pub fn get_cell(num: usize) -> String {
    match num {
        1 => format!(
            "{}{}{}{}{}",
            "           ",
            "    ▄█     ",
            "     █     ",
            "     █     ",
            "           ",
        ),
        2 => format!(
            "{}{}{}{}{}",
            "           ",
            "   ▄▀▀▀▄   ",
            "     ▄▀    ",
            "   ▄█▄▄▄   ",
            "           ",
        ),
        3 => format!(
            "{}{}{}{}{}",
            "           ",
            "   ▄▀▀▀▄   ",
            "      ▀▄   ",
            "   ▀▄▄▄▀   ",
            "           ",
        ),
        4 => format!(
            "{}{}{}{}{}",
            "           ",
            "    █      ",
            "    █▄█▄   ",
            "      █    ",
            "           ",
        ),
        5 => format!(
            "{}{}{}{}{}",
            "           ",
            "   █▀▀▀▀   ",
            "   ▀▀▀▀▄   ",
            "   ▀▄▄▄▀   ",
            "           ",
        ),
        6 => format!(
            "{}{}{}{}{}",
            "           ",
            "    ▄▀▀    ",
            "    █▀▀▄   ",
            "    ▀▄▄▀   ",
            "           ",
        ),
        7 => format!(
            "{}{}{}{}{}",
            "           ",
            "   ▀▀▀▀█   ",
            "      █    ",
            "     █     ",
            "           ",
        ),
        8 => format!(
            "{}{}{}{}{}",
            "           ",
            "   ▄▀▀▀▄   ",
            "   ▄▀▀▀▄   ",
            "   ▀▄▄▄▀   ",
            "           ",
        ),
        9 => format!(
            "{}{}{}{}{}",
            "           ",
            "    ▄▀▀▄   ",
            "    ▀▄▄█   ",
            "     ▄▄▀   ",
            "           ",
        ),
        _ => String::new(),
    }
}

